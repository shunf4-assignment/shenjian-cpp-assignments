#include <iostream>
using namespace std;
int main()
{
	int a, b, c;
	a = 10;
	b = 23;
	c = a + b;
	cout << "a+b=";
	cout << c;
	cout << endl;
	return 0;
}
/* 程序的运行过程：
定义a,b,c为整形变量，将10,23分别赋值给a,b，再计算a+b的结果赋值给c。之后输出"a+b="和计算结果c。
运行结果：在屏幕上输出：
-a+b=33
并且换行，之后程序结束执行。*/